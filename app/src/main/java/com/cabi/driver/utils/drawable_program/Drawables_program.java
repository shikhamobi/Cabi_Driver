package com.cabi.driver.utils.drawable_program;

import android.graphics.drawable.GradientDrawable;
import android.view.View;

import com.cabi.driver.R;
import com.cabi.driver.utils.CL;

/**
 * Created by developer on 7/3/17.
 */
public class Drawables_program {
    public static View shift_bg_grey(View v) {


        GradientDrawable drawable = (GradientDrawable) v.getBackground();
        GradientDrawable gdDefault = new GradientDrawable();
        gdDefault.setColor(CL.getColor(R.color.shiftoffcolor));
        gdDefault.setCornerRadius(50);
        gdDefault.setStroke(1, CL.getColor(R.color.shiftoffcolor));
        v.setBackgroundDrawable(gdDefault);
        return v;
    }
    public static View shift_on(View v) {


        GradientDrawable drawable = (GradientDrawable) v.getBackground();
        GradientDrawable gdDefault = new GradientDrawable();
        gdDefault.setColor(CL.getColor(R.color.shiftoncolor));
        gdDefault.setCornerRadius(50);
        gdDefault.setStroke(1, CL.getColor(R.color.shiftoncolor));
        v.setBackgroundDrawable(gdDefault);
        return v;
    }
}
